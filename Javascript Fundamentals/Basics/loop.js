// while loop
// do while loop
// code block first, condition second
// run at least
// for loop

// while
let amount = 10;
while (amount > 0) {
    console.log('I have ' + amount + ' $');
    amount--;
}

// do while
// it run at least one time even if condition does not satisfy
let count = 15;
do {
    console.log('I have ' + count + ' $');
    count++;
} while (count < 10);

// for loop

for (let i = 0; i <= 10; i++) {
    console.log('You are in the floor number ' + i + '.');
}